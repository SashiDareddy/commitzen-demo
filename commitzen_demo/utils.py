from math import sqrt


def add_sqrts(a: float, b: float) -> float:
    """Add the square roots of two numbers"""
    return sqrt(a) + sqrt(b)


def multiply_sqrts(a: float, b: float) -> float:
    """Multiply the square roots of two numbers"""
    return sqrt(a) * sqrt(b)


def divide_sqrts(a: float, b: float) -> float:
    """Divide the square roots of two numbers"""
    return sqrt(a) * sqrt(b)